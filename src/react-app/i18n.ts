const savedLanguage =
  typeof window !== 'undefined' ? window.localStorage.getItem('cpp:language') : null

void i18n.use(initReactI18next).init({
  resources,
  lng: savedLanguage || 'zh-CN',
  fallbackLng: 'en',
  interpolation: {
    escapeValue: false,
  },
})

if (typeof window !== 'undefined') {
  i18n.on('languageChanged', (language) => {
    window.localStorage.setItem('cpp:language', language)
    document.documentElement.lang = language
  })
  document.documentElement.lang = i18n.language
}

export default i18n
